    public function __construct($key, $text)
    {
        $this->key = strtoupper($key);
        $this->text = strtoupper($text);
    }

    public function encrypt()
    {
        $result = '';
        $keyLength = strlen($this->key);
        $keyIndex = 0;

        for ($i = 0; $i < strlen($this->text); $i++) {
            $char = $this->text[$i];

            if (($charPos = strpos($this->alphabet, $char)) !== false) {
                $keyChar = $this->key[$keyIndex % $keyLength];
                $keyPos = strpos($this->alphabet, $keyChar);

                $result .= $this->table[$charPos][$keyPos];
                $keyIndex++;
            } else {
                $result .= $char;
            }
        }

        return $result;
    }

    public function decrypt()
    {
        $result = '';
        $keyLength = strlen($this->key);
        $keyIndex = 0;

        for ($i = 0; $i < strlen($this->text); $i++) {
            $char = $this->text[$i];

            if (($keyPos = strpos($this->alphabet, $this->key[$keyIndex % $keyLength])) !== false) {
                for ($j = 0; $j < 26; $j++) {
                    if ($this->table[$j][$keyPos] === $char) {
                        $result .= $this->alphabet[$j];
                        break;
                    }
                }

                $keyIndex++;
            } else {
                $result .= $char;
            }
        }

        return $result;
    }
